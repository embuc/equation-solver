package se.emirbuc.solver.symbols;

/**
 * The <code>Interface Symbols</code>. All the characters that are supported for arithmetic evaluation.
 *
 * @author Emir Bucalovic (embuc)
 * @since 2015-okt-13
 */
@SuppressWarnings("javadoc")
public interface Symbols {
	public static final char PLUS = '+';
	public static final char MINUS = '-';
	public static final char MULTIPLICATION = '*';
	public static final char DIVISION = '/';
	public static final char LEFT_PARANTHESIS = '(';
	public static final char RIGHT_PARANTHESIS = ')';
	public static final char POWER_EXPONENT = '^';
}
